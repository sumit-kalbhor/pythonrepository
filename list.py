# create a list
int_list = [1, 2, 3, 4]
print(int_list)

mixed_list = [10.00, "Sumit", 1, True]
print(mixed_list)

# list() function creates a list
name = list("SUMIT")
print(name)

# in or not in operators
print('S' in name)
print('K' in name)
print('K' not in name)

str_list = ["one", "two", "three"]
print(str_list[1])

# list slicing

sliced = [1, 2, 3, 4, 5, 6]
print(sliced[2:4])
print(sliced[0:])


# update a list using index numbers

sliced[1] = 10
sliced[2:4] = [11, 12]
print(sliced)
print(sliced[3])

